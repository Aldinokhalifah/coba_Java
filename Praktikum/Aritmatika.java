public class Aritmatika {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;

        System.out.println("Penjumlahan:" + (a + b));
        System.out.println("Pengurangan:" + (a - b));
        System.out.println("Pengalian:" + (a * b));
        System.out.println("Pembagian:" + (a / b));
    }
}
