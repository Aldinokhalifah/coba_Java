public class Main04 {
    public static void main(String[] args) {
        int nilai = 85; // Inisialisasi variabel nilai

        // Statement if-else if untuk menentukan grade berdasarkan nilai
        if (nilai >= 90) {
            System.out.println("Grade: A");
        } else if (nilai >= 80) {
            System.out.println("Grade: B");
        } else if (nilai >= 70) {
            System.out.println("Grade: C");
        } else {
            System.out.println("Grade: D");
        }
    }
}